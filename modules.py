class TMEncoder(nn.Module):
    def __init__(self, tm_start_size, enc_sizes):
        super().__init__()
        enc_layers = [torch.nn.Linear(tm_start_size,enc_sizes[0])]
    
        last_size = enc_sizes[0]
        for ly_size in enc_sizes:
            enc_layers.append(torch.nn.ReLU())
            enc_layers.append(torch.nn.Linear(last_size,ly_size))
            last_size = ly_size
    
        self.net = torch.nn.Sequential(*enc_layers)
  
    def forward(self, x):
        return self.net(x)



class CoMBD(torch.nn.Module):

    def __init__(self, filters, kernels, groups, strides, use_spectral_norm=False):
        super(CoMBD, self).__init__()
        norm_f = weight_norm if use_spectral_norm == False else spectral_norm
        self.convs = nn.ModuleList()
        init_channel = 1
        for i, (f, k, g, s) in enumerate(zip(filters, kernels, groups, strides)):
            self.convs.append(norm_f(Conv1d(init_channel, f, k, s, padding=get_padding(k, 1), groups=g)))
